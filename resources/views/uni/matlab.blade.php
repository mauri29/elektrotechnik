<h3>Matlab Workshop</h3>
<p><b>Nummer:</b> matlb. <b>ECTS:</b> 3.0. <b>Niveau:</b> intermediate</p>
<p>
    <b>Inhalt:</b>
    <ol>
        <li><b>Einführung mit Matlab</b></li>
        <ul>
            <li>Beispiele, Demos, Taschenrechner</li>
            <li>Arbeiten auf der command-Ebene (help, doc etc.)</li>
            <li>Erstellen von m-files (Programme)</li>
        </ul>
        <li><b>Mathematik mit Matlab</b></li>
        <ul>
            <li>Einfache mathematische Funktionen aus der Analysis</li>
            <li>Einfache mathematische Funktionen aus der Algebra</li>
            <li>Differentiation, Integration</li>
            <li>Anwendungen der linearen Algebra (Matrizen)</li>
        </ul>
        <li><b>Grafiken mit Datenausgabe</b></li>
        <ul>
            <li>Koordinatensysteme</li>
            <li>Plotten</li>
            <li>3D-Darstellungen</li>
        </ul>
        <li><b>Programmieren mit Matlab</b></li>
        <ul>
            <li>Programm-Ablaufsteuerung (Schleifen, Bedingungen)</li>
            <li>Programmierübungen</li>
        </ul>
    </ol>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden kennen den Aufbau der Mathematik-Software MATLAB. Sie können mathematische Probleme im Command-Window oder mit Hilfe von Skripts lösen. Sie kennen das eingebaute Hilfesystem und können dieses nutzen.</li>
        <li>Die Studierenden kennen die wichtigsten Befehle von MATLAB um Problemstellungen aus der Analysis und der linearen Algebra zu lösen.</li>
        <li>Die Studierenden kennen die verschiedenen Visualisierungsmöglichkeiten von MATLAB und können erstellte Grafiken weiter bearbeiten.</li>
        <li>Die Studierenden können für Ingenieursaufgaben Programme mit Schleifen, Verzweigungen, Vektoren und Matrizen erstellen. Sie können Programme debuggen sowie die Funktionsweise fremden Quellcodes analysieren und beschreiben.</li>
        <li>Die Studierenden können ihre Problemlösungen und eigene Programme ausreichend dokumentieren und beschreiben.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Analysis 1 (an1), Lineare Algebra 1 (lalg1)</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote</p>
