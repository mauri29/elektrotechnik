<h3>Analysis 1</h3>
<p><b>Nummer:</b> an1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><b>Inhalt: </b>
<ol>
    <li><b>Grundbegriffe der Mengenlehre</b>
    <ul>
        <li>Zahlenmengen</li>
        <li>Punktmengen in der Ebene und dem Raum</li>
        <li>Mengenrelationen und -operationen</li>
    </ul></li>
    <li><b>Funktionen</b>
    <ul>
        <li>Funktionsbegriff</li>
        <li>affine Funktionen</li>
        <li>Potenzfunktionen</li>
        <li>Polynomfunktionen</li>
        <li>trigonometrische Funktionen</li>
        <li>Exponentialfunktionen und ihre Umkehrungen</li>
        <li>Eigenschaften von Funktionen: unter anderem Symmetrien, Stetigkeit, Singularitäten</li>
        </li>
    </ul>
    <li><b>Grenzwerte</b>
    <ul>
    <li>Grenzwerte von Zahlenfolgen</li>
    <li>Grenzwerte von Funktionen</li>
    <li>Rechnen mit Grenzwerten</li>
    <li>Differenzenquotient, Tangentenproblem</li>
    </ul>
    </li>
    <li><b>Differenzialrechnung</b>
    <ul>
    <li>Differenzialquotient, Tangentenproblem</li>
    <li>Ableitungsregeln: Linearität, Produktregel, Quotientenregel, Kettenregel</li>
    <li>Ableitungen einfacher Funktionen</li>
    <li>Anwendungen: Extremwertprobleme, Kurvendiskussion, ev. Tangentenverfahren von Newton oder Regel von de l'Hospital</li>
    </ul>
    </li>
    <li><b>Einführung Integralrechnung</b>
    <ul>
    <li>bestimmtes Integral</li>
    <li>unbestimmtes Integral</li>
    <li>Integrationsregeln: Linearität, Additivität, Hauptsatz der Integralrechnung</li>
    <li>Flächenberechnungen </li>
    </ul>
    </li>
</ol>
</p>
<p><b>Lernziele:</b>
<ul>
    <li>Die Studierenden wissen, was eine Menge ist, sind mit den Notationen vertraut und können diese verwenden.</li>
    <li>Die Studierenden kennen die elementaren Funktionen und deren Eigenschaften und können sie ohne elektronische Hilfsmittel visualisieren.</li>
    <li>Die Studierenden verstehen das Konzept des Grenzwertes und können in einfachen Fällen Grenzwerte von Folgen und Funktionen berechnen.</li>
    <li>Die Studierenden verstehen die Ableitung als Tangentensteigung und Änderungsrate einer Funktion und können sie von elementaren und zusammengesetzten Funktionen formal berechnen.</li>
    <li>Die Studierenden verstehen das Konzept des Integrals als Fläche unter der Kurve und als Stammfunktion und können die Stammfunktion von einigen elementaren Funktionen berechnen.</li>
    <li>Die Studierenden können obige Konzepte auf einfache Probleme der Technik anwenden (Extremwerte, Flächenberechnung, ev. Nullstellensuche)</li>
</ul>
</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote und schriftliche MSP</p>
