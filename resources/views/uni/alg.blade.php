<h3>Algebra</h3>
<p><b>Nummer:</b> alg. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><b>Inhalt: </b>
<ol>
    <li><b>Komplexe Zahlen:</b>
        <ul>
            <li>Gauss'sche Zahlenebene, Formel von Moivre</li>
            <li>Exponentialfunktion im Komplexen; Potenzen und Wurzeln</li>
            <li>Komplexe Darstellung harmonischer Schwingungen</li>
            <li>Ortskurven, inverses Bild von Gerade und Kreis</li>
            <li>Wechselstromschaltungen, Impedanzen</li>
        </ul>
    </li>
    <li><b>Polynome, rationale Funktionen</b>
        <ul>
            <li>Fundamentalsatz der Algebra; Nullstellenbestimmung</li>
            <li>Partialbruchzerlegung</li>
            <li>Binomischer Satz</li>
        </ul>
    </li>
    <li><b>Begriff des Vektorraums, Basis, lineare Abhängigkeit</b>
        <ul>
            <li>Lineare Abbildungen, Darstellung mit Matrizen</li>
            <li>Rang einer Matrix, Bild und Kern, Dimensionssatz</li>
        </ul>
    </li>
</ol>
</p>
<p><b>Lernziele:</b>
    <ul>
        <li>Die Studierenden kennen komplexe Zahlen in ihren verschiedenen Darstellungsformen, können komplexe Zahlen visualisieren und mit ihnen Berechnungen durchführen (Grundrechenarten, Potenzen, Wurzeln).</li>
        <li>Sie verstehen die Exponentialfunktion mit komplexen Argumenten und ihren Zusammenhang mit harmonischen Schwingungen.</li>
        <li>Sie können Wechselstromschaltungen mit komplexen Impedanzen darstellen und daraus die technisch relevanten Grössen (Amplitudenverhältnis und Phasenverschiebung zwischen Strom und Spannung) bestimmen.</li>
        <li>Die Studierenden kennen verschiedene Darstellungen von Polynomen und können diese in ein Produkt von Linearfaktoren zerlegen.</li>
        <li>Sie verstehen die Bedeutung von Vektorräumen und das Konzept von Basis und linearer Unabhängigkeit.</li>
        <li>Sie können lineare Abbildungen mit Matrizen darstellen und die Verkettung von Abbildungen durch Matrizenmultiplikation berechnen.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Lineare Algebra 1 (lalg1)</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote und schriftliche MSP</p>
