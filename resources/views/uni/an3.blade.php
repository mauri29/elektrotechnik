<h3>Analysis 3</h3>
<p><b>Nummer:</b> an3E. <b>ECTS:</b> 3.0. <b>Niveau:</b> intermediate</p>
<p><b>Inhalt:</b>
<p><ol>
    <li><b>Fourier-Reihen</b>
        <ul>
            <li>Einführung Fourier-Reihen</li>
            <li>Konvergenz von Fourier-Reihen</li>
            <li>Fourier-Reihen in reeller Darstellung</li>
            <li>Approximation periodischer Funktionen durch trigonometrische Polynome, Fehlerabschätzung</li>
            <li>Repetition Komplexe Zahlen</li>
            <li>Fourier-Reihen in komplexer Darstellung</li>
            <li>Phasen- und Amplitudendarstellung</li>
        </ul>
    </li>
    <li><b>Laplace-Transformation</b>
        <ul>
            <li>Repetition Partialbruchzerlegung</li>
            <li>Definition Laplace-Transformation, Beschreibung von Funktionen durch die Laplace-Transformierte</li>
            <li>Eigenschaften der Laplace-Transformation (Linearität, Verschiebung, Differentiation)</li>
            <li>Anwendung bei linearen Differentialgleichungen</li>
        </ul>
    </li>
    <li><b>Funktionen mehrerer Variablen</b>
        <ul>
            <li>Definition, Niveaulinien</li>
            <li>Partielle Ableitungen und geometrische Bedeutung</li>
            <li>Höhere Ableitungen und der Satz von Schwarz</li>
            <li>Tangentialebene an eine Fläche, vollständiges Differenzial</li>
            <li>Richtungsableitung</li>
            <li>Gradient einer Funktion</li>
        </ul>
    </li>
    <li><b>Extremstellen bei mehreren Variablen</b>
        <ul>
            <li>Stationäre Punkte</li>
            <li>Art der Extremalstellen</li>
            <li>Anwendung: Methode der kleinsten Quadrate</li>
        </ul>
    </li>
    <li><b>Vektoranalysis</b>
        <ul>
            <li>Vektorfelder</li>
            <li>Arbeitsintegrale</li>
            <li>Potentialfelder</li>
        </ul>
    </li>
    <li><b>MATLAB</b>
        <ul>
            <li>Funktionen und Gleichungen in MATLAB (Visualisierung)</li>
            <li>Symbolisches Integrieren mit MATLAB</li>
            <li>Laplace Transformation und deren Anwendung</li>
            <li>Arbeiten mit Funktionen mit mehreren Veränderlichen</li>
        </ul>
    </li>
</ol>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden kennen die Fourier-Reihen in reeller und in komplexer Darstellung und können periodische Funktionen durch eine Fourier-Reihe beschreiben.</li>
        <li>Die Studierenden kennen die Laplace-Transformation und können Funktionen durch eine Laplace-Transformierte beschreiben. Die Studierenden können mit der Laplace-Transformation lineare Differentialgleichungen untersuchen.</li>
        <li>Die Studierenden kennen Funktionen mehrerer Variablen und können diese für zwei Variablen visualisieren. Sie können partielle erste und höhere Ableitungen berechnen und kennen die geometrischen Bedeutungen davon. Sie kennen die Bedeutung des Satzes von Schwarz.</li>
        <li>Die Studierenden können die Richtungsableitung und den Gradienten einer Funktion mehrerer Variablen berechnen und kennen deren geometrische Bedeutung.</li>
        <li>Die Studierenden können von Funktionen mehrerer Variablen stationäre Punkte finden und die Art der Extremstellen bestimmen. </li>
        <li>Die Studierenden kennen das Prinzip des minimalen quadratischen Fehlers und können dieses anwenden.</li>
        <li>Die Studierenden kennen das Arbeitsintegral. Sie können entscheiden, ob ein Arbeitsintegral unabhängig vom Weg ist oder nicht und können dieses in beiden Fällen berechnen.</li>
        <li>Die Studierenden können obige Konzepte auf Probleme der Technik anwenden.</li>
        <li>Die Studierenden können die mathematischen Techniken die im Modul behandelt werden auch mit MATLAB anwenden. Sie können Anwendungsaufgaben mit MATLAB lösen und das Tool für Visualisierungen nutzen. Im Weiteren können die Studierenden das Tool für korrektes wissenschaftliches Arbeiten nutzen.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse: </b> Analysis 2 (an2) und Algebra (alg)</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote und schriftliche MSP</p>
