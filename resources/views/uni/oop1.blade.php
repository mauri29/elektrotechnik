<h3>Objektorientierte Programmierung 1</h3>
<p><b>Nummer:</b> oop1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p>
    <b>Inhalt:</b>
    <ul>
        <li>Variablen und Berechnungen: primitive Datentypen, Wertebereiche und deren Auswirkung beim Rechnen</li>
        <li>Methoden und Parameter: Deklaration, Aufruf, Signatur, Argumente und Rückgabewert</li>
        <li>Kontrollstrukturen: Schleifen und Fallunterscheidungen</li>
        <li>Grundlagen Klassen, Objekte und Vererbung: Deklaration, Instanziierung von Klassen, Überladen und Überschreiben von Methoden, Vererbung, Polymorphie</li>
        <li>Grundlagen Grafik und Benutzerschnittstellen: Schaltflächen, Textfelder etc., Behandlung der zugehörigen Ereignisse</li>
    </ul>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden verstehen die grundlegenden Sprachkonstrukte und Datentypen der Programmiersprache Java.</li>
        <li>Sie sind in der Lage, einfache Methoden zu schreiben und die Parameter sinnvoll zu wählen.</li>
        <li>Die Studierenden verstehen das Konzept von Klassen und Instanzen und können selbst einfache Klassen inklusive Vererbung schreiben.</li>
        <li>Sie sind in der Lage, einfache Grafik und Benutzerschnittstellen zu programmieren.</li>
        <li>Sie kennen die Ereignisse und die zugehörigen Empfänger der einfachen Eingabeelemente.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> keine</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote</p>
