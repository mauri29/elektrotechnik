<h3>Digitaltechnik 1</h3>
<p><b>Nummer:</b> dt1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p>
    <b>Inhalt:</b>
    <ul>
        <li>Repetition dualer Zahlensysteme und Boolesche Algebra</li>
        <li>kombinatorische Logik und Grundschaltungen</li>
        <li>Flipflop-Typen und -Schaltungen</li>
        <li>synchrone Schalttechnik und Synchronisation von Eingangssignalen</li>
        <li>Kenn- und Grenzwerte sowie digitale Interface-Schaltungen</li>
        <li>aktuelle digitale Technologien</li>
        <li>Zustandsmaschinen und deren digitaltechnische Umsetzung</li>
        <li>Speichertechnologien</li>
    </ul>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden verstehen die Funktionsweise und die statischen und dynamischen Eigenschaften von digitalen Komponenten inklusive Speicherbausteine.</li>
        <li>Sie können Datenblätter von digitalen Komponenten interpretieren und einfache kombinatorische sowie synchrone Digitalschaltungen auf Schemaebene fachgerecht entwickeln.</li>
        <li>Die Studierenden kennen die Probleme, welche beim Verschalten unterschiedlicher Digital-Technologieren auftreten können.</li>
        <li>Die Studierenden können Zustandsdiagramme sowohl interpretieren als auch erstellen und diese in eine synchrone Schaltung umsetzen und haben mit einem gängigen CPLD-/FPGA-Entwicklungstool auf Schemaebene gearbeitet.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Konvergenz Elektronik (konvE)</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote</p>