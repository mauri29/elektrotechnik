<h3>Grundlagenlabor 1</h3>
<p><b>Nummer:</b> glaL1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p>
    <b>Inhalt:</b>
    <ul>
        <li>Grundlegende Messungen an Bauelementen und Schaltungen der Elektrotechnik und Elektronik</li>
        <li>Gleichstrommessgeräte</li>
        <li>Widerstandsmessung (Vier-Draht-Methode)</li>
        <li>Quellen, Ersatzschaltung und Kennlinien, auch nichtlineare</li>
        <li>Oszilloskop (Darstellung von zeitabhängigen Signalen)</li>
        <li>Bauelemente (Dioden, Transistoren, Inverter, OP)</li>
    </ul>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden machen sich mit den wichtigsten Labor-Messgeräten und grundlegenden Messmethoden der Elektrotechnik und Elektronik vertraut.</li>
        <li>Sie können mit typischen Messgeräten (z.B. Voltmeter, Amperemeter, Oszilloskop usw.) elektrische Grössen sachgemäss und korrekt messen und die Ergebnisse kontextgerecht interpretieren.</li>
        <li>Die Studierenden kennen typische Grundschaltungen der Elektronik und Digitaltechnik.</li>
        <li>Sie können diese elektronischen Schaltungen analysieren, simulieren und mit modernen Messmitteln charakterisieren.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Die Studierenden werden anhand ihrer beruflichen Vorkenntnisse in die entsprechende Gruppe eingeteilt</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote</p>
