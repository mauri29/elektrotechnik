<h3>Mechanik</h3>
<p><b>Nummer:</b> mech. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p>
    <b>Inhalt:</b>
    <ul>
        <li>Masse, Massenpunkt, Starrer Körper, Schwerpunkt, Massenträgheitsmoment, Impuls, Drehimpuls, Kraft, Drehmoment</li>
        <li>Kinematik mit Hilfe der Differentialrechnung</li>
        <li>Newtonsche Axiome, Gravitationskraft, Reibungskräfte, Federkräfte, Schwerpunktsatz, Impuls, Drehimpuls, Arbeit, Leistung, Energie, Erhaltungssätze, Stossprozesse, Beschreibung der Dynamik aus Sicht des mitbewegten Beobachters, Trägheitskräfte</li>
    </ul>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden können für ein mechanisches Problem (System von Punktmassen oder starre Körper) die Bewegungsgleichungen auf der Basis der Newtonschen Axiome aufstellen und lösen.</li>
        <li>Die Studierenden können ein mechanisches System in einfachere Teilsysteme zerlegen</li>
        <li>Die Studierenden kennen das Konzept von Kraftstoss resp. Drehmomentstoss und können dieses auf mechanische Probleme anwenden.</li>
        <li>Die Studierenden kennen das Konzept der Erhaltungsgrössen der Mechanik (Impulserhaltung, Drehimpulserhaltung und Energieerhaltung) und können diese korrekt auf Systemen mit Massenpunkten und starren Körpern anwenden.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse: </b>Analysis 1 (an1)</p>
<p><b>leistungsbewertung:</b> Erfahrungsnote und schriftliche MSP</p>
