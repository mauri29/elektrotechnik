<h3>Analysis 2</h3>
<p><b>Nummer:</b> an2. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><b>Inhalt: </b>
<p>
<ol>
<li><b>Integralrechnung</b>
    <ul>
        <li>Riemannsches Integral</li>
        <li>Integrationsmethoden: Substitution, partielle Integration.</li>
        <li>uneigentliche Integrale</li>
    </ul>
</li>
<li><b>Umkehrfunktionen</b>
    <ul>
        <li>Definition der Umkehrfunktion.</li>
        <li>Arkusfunktionen und deren Ableitungen.</li>
    </ul>
</li>
<li><b>Hyperbelfunktionen und ihre Umkehrfunktionen</b>
    <ul>
        <li>Definition des Sinus-, Kosinus- und Tangenshyperbolicus</li>
        <li>Beispiele: Kettenlinie</li>
        <li>Areafunktionen und deren Ableitungen</li>
    </ul>
</li>
<li><b>Volumen-, Oberflächen- und Bogenlängenberechnungen</b>
    <ul>
        <li>Volumen von Rotationskörpern und verallgemeinerten Körpern</li>
        <li>Bogenlänge einer Kurve</li>
        <li>Mantelfläche von Rotationskörpern</li>
    </ul>
</li>
<li><b>Unendliche Reihen</b>
    <ul>
        <li>Reihen: geometrische Reihe, hyperharmonische Reihe, Konvergenzkriterien</li>
        <li>Potenzreihen: geometrische Bedeutung, Methoden zur Reihenentwicklung, Konvergenzbereich</li>
    </ul>
</li>
</ol>
</p>
<p><b>Lernziele:</b>
<ul>
    <li>Die Studierenden kennen das Prinzip der Integralberechnung mit einer Riemannschen Summe. Sie können Integrale, die im Ingenieurwesen vorkommen mittels Substitution und partieller Integration (und ev. mit Partialbruchzerlegung) berechnen.</li>
    <li>Die Studierenden können die Umkehrfunktionen von elementaren Funktionen berechnen und kennen deren Eigenschaften.</li>
    <li>Die Studierenden kennen die Hyperbelfunktionen und ihre Umkehrfunktionen und können diese anwenden. Sie kennen das Beispiel der Kettenlinie.</li>
    <li>Die Studierenden können Volumen von Rotationskörpern und Mantelflächen berechnen. Sie können Längen von Kurven berechnen.</li>
    <li>Die Studierenden kennen ebene parametrisierte Kurven und können diese anwenden und kennen den Krümmungsbegriff von Kurven.</li>
    <li>Die Studierenden kennen die geometrische und die harmonische Reihe und können von einer Reihe bestimmen, ob sie konvergiert. Sie können von elementaren Funktionen Taylorreihen berechnen und können von einer Potenzreihe den Konvergenzbereich bestimmen.</li>
    <li>Die Studierenden können obige Konzepte auf Probleme der Technik anwenden.</li>
</ul>
</p>
