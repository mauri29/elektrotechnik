<h3>Lineare Algebra 1</h3>
<p><b>Nummer:</b> lalg1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><b>Inhalt: </b>
<ul>
    <li>Lineare Gleichungssysteme: Gauss-Algorithmus, Rang, Lösbarkeit, Lösungsverhalten</li>
    <li>Matrizenrechnung: Rechenoperationen, Transponieren, Determinante, inverse Matrix</li>
    <li>Vektorrechnung bis Vektor- und Spatprodukt, Anwendung auf Geometrie von Punkten, Geraden und Ebenen im Raum</li>
    <li>Lineare Abbildungen: Darstellung mit Matrizen, Drehung, Spiegelung und
    Projektion</li>
</ul>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden kennen lineare Gleichungssysteme, können solche aufstellen und lösen; sie können die Lösbarkeit eines Gleichungssystems beurteilen und die Lösungsmenge bestimmen.</li>
        <li>Sie kennen Vektoren im zwei- und dreidimensionalen Raum und können Rechenoperationen mit Vektoren ausführen und bei typischen Problemstellungen anwenden.</li>
        <li>Sie können geometrische Objekte (Geraden, Ebenen) analytisch beschreiben und geometrische Problemstellungen untersuchen und lösen.</li>
        <li>Die Studierenden kennen Matrizen und deren spezifische Eigenschaften und können mit Matrizen rechnen (nichtkommutative Multiplikation, zwei- und dreireihige Determinanten, Rechenregeln mit Transponierten und Inversen).</li>
        <li>Sie können lineare Abbildungen, insbesondere Drehungen und Spiegelungen, mit Matrizen darstellen und die Abbildung von Punkten durch Matrizenmultiplikation berechnen.</li>
        <li>Sie können die im Modul behandelten geometrischen und technischen Anwendungsprobleme mit MATLAB lösen.</li>
    </ul>
</p>
