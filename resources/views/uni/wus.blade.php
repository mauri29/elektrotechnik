<h3>Wärme und Strahlung</h3>
<p><b>Nummer:</b> mech. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><ul>
    <li>Grundbegriffe der Wärmelehre: Wärmefluss, Temperatur, Wärmekapazität, Kalorimetrie</li>
    <li>phänomenologische Behandlung der grundlegenden Wärmetransportmechanismen (Wärmeleitung, Konvektion und Wärmestrahlung)</li>
    <li>Temperaturstrahler und nicht-thermische Lichtquellen</li>
    <li>Grundlagen der Radiometrie und Photometrie (Begriff des Spektrums, Radiometrische und photometrische Messgrössen, Reflexion, Absorption und Transmission von elektromagnetischer Strahlung, reiner Strahlungsaustausch</li>
    <li>Grundlagen der geometrischen Optik: Abbildungsgleichung, optische Abbildungen, einfache optische Instrumente</li>
</ul>
</p>
<p><b>Lernziele:</b>
<ul>
    <li>Die Studierenden verstehen die Grundbegriffe Arbeit, Energie und Leistung (im mechanischen und thermischen Bereich) und können einfache Energiebilanzen aufstellen und lösen und interpretieren</li>
    <li>Die Studierenden können die Wärmeübertragung für einfache geometrische Anordnungen berechnen</li>
    <li>Die Studierenden können die wesentlichen phänomenologischen Eigenschaften der elektromagnetischen Strahlung beschreiben</li>
    <li>Die Studiedenren können den Energieaustausch über Strahlung zwischen zwei Körpern berechnen</li>
    <li>Die Studierenden können die wesentlichen Mechanismen zur Erzeugung von Licht erklären</li>
    <li>Die Studierenden können die Abbildungseigenschaften von Spiegeln und Linsen formulieren und den Strahlengang in einfachen optischen Instrumenten konstruieren und berechnen</li>
</ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Analysis 1 (an1)</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote und schriftliche MSP</p>
