<h3>Konvergenz Elektrotechnik</h3>
<p><b>Nummer:</b> konvE. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p>
    <b>Inhalt:</b>
    <ol>
        <li><b>Teil Grundzüge der Digitaltechnik</b></li>
        <ul>
            <li>Zahlensysteme: allgemeine Eigenschaften, duales (binäres) Zahlensystem, Hexadezimalsystem und erweitertes binäres Zahlensystem</li>
            <li>Grundlagen der Booleschen Algebra: binäre Verknüpfungen, Gesetze von Boole und De Morgan sowie Gesetze der Schaltalgebra</li>
            <li>Schaltungsdesign: Analysieren von digitalen Schaltungen, Umsetzung der Booleschen Gleichungen in digitale Schaltungen (Synthese) und Arbeiten mit Simulationstools</li>
        </ul>
        <li><b>Teil Programmieren</b></li>
        <ul>
            <li>Variablen und Berechnungen: primitive Datentypen, Wertebereiche und deren Auswirkung beim Rechnen.</li>
            <li>Methoden und Parameter: Deklaration und Aufruf, Signatur, Argumente und Rückgabewert.</li>
            <li>Kontrollstrukturen: Schleifen und Fallunterscheidungen.</li>
            <li>Grundlagen Klassen und Objekte: Deklaration und Instanziierung von Klassen.</li>
        </ul>
    </ol>
</p>
<p>
    <b>Lernziele:</b>
    <ul>
        <li>Die Studierenden kennen unterschiedliche Zahlensysteme mit deren Eigenschaften und können darin einfache Rechnungen durchführen; die Systeme können transformiert werden. Die Studierenden wenden Gesetze der Schaltalgebra nach Boole und De Morgan an. Einfache Fragestellungen aus dem technischen Bereich können die Studierenden zu funktionierenden digitalen Netzwerken entwickeln (synthetisieren), diese optimieren und für verschiedene Technologien der Digitaltechnik darstellen.</li>
        <li>Die Studierenden verstehen die grundlegenden Sprachkonstrukte und Datentypen der Programmiersprache Java. Sie sind in der Lage, einfache Methoden zu schreiben und die Parameter sinnvoll zu wählen. Die Studierenden verstehen das Konzept von Klassen und Instanzen und können selbst einfache Klassen schreiben.</li>
    </ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> keine</p>
<p><b>Leistungsbewertung:</b> Testat</p>
