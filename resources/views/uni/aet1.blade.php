<h3>Allgemeine Elektrotechnik 1</h3>
<p><b>Nummer:</b> aet1. <b>ECTS:</b> 3.0. <b>Niveau:</b> basic</p>
<p><b>Inhalt:</b>
<ul>
    <li>Grundbegriffe und Grundgesetze bei Gleichstrom.</li>
    <li>Spannung, Strom und Widerstand.</li>
    <li>Analyse von einfachen Gleichstromkreisen.</li>
    <li>Einfache RLC-Netzwerke im Zeitbereich.</li>
    <li>Periodische Zeitabhängigkeit.</li>
    <li>Ein- und Ausschaltvorgänge.</li>
</ul>
</p>
<p><b>Lernziele:</b>
<ul>
    <li>Die Studierenden verstehen die grundlegenden Zusammenhänge der Gleichstromlehre und können sie interpretieren und erklären.</li>
    <li>Sie kennen die Methoden und Verfahren zur Berechnung von Gleichstromnetzwerken und können sie an Beispielen anwenden.</li>
    <li>Sie kennen die Grundgesetze für Widerstand, Spule und Kondensator und können sie für beliebige Zeitabhängigkeit erläutern. </li>
    <li>Die Studierenden kennen und verstehen Ein- und Ausschaltvorgänge und können damit einfache Beispiele berechnen (ein Speicherelement).</li>
    <li>Sie können bei harmonischer Zeitabhängigkeit den Zusammenhang von Strom und Spannung bei Spule und Kondensator erklären.</li>
    <li>Sie sind vertraut und geübt mit dem Ingenieurshandwerk und können ihre Berechnungen und Ergebnisse verifizieren.</li>
</ul>
</p>
<p><b>Empfohlene Vorkenntnisse:</b> Analysis 1 (an1).</p>
<p><b>Leistungsbewertung:</b> Erfahrungsnote und MSP schriftlich.</p>
